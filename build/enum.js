"use strict";
/**
 * enum =>sama konsep nya dengan enum dart
 */
/**
 * numeric enum => berdasarkan index
 * string enum => berdasarkan string
 */
var Month;
(function (Month) {
    Month[Month["JAN"] = 0] = "JAN";
    Month[Month["FEB"] = 1] = "FEB";
    Month[Month["MAR"] = 2] = "MAR";
    Month[Month["APR"] = 3] = "APR";
})(Month || (Month = {}));
var Day;
(function (Day) {
    Day["Sen"] = "Senin";
    Day["Sel"] = "Selasa";
    Day["Ra"] = "Rabu";
})(Day || (Day = {}));
